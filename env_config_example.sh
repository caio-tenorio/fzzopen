# Example configuration using environment variables
# Add to your ~/.bashrc, ~/.zshrc, or config.fish

# Preferred editor for text files
export FOPEN_TEXT_EDITOR="nvim"

# Preferred file manager
export FOPEN_FILE_MANAGER="nautilus"

# Preferred image viewer
export FOPEN_IMAGE_VIEWER="loupe"

# Preferred PDF viewer
export FOPEN_PDF_VIEWER="okular"

# Directories to exclude (colon-separated)
export FOPEN_EXCLUDE_DIRS=".git:node_modules:.vscode:.idea:dist:build:target:.cache"

# Use fd if available (true/false)
export FOPEN_USE_FD="true"

# fzf height
export FOPEN_FZF_HEIGHT="90%"

# Show hidden files by default (true/false)
export FOPEN_SHOW_HIDDEN="false"
